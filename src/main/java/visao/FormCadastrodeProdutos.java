    private void btnCadastroProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastroProdActionPerformed

           String nome = inputTextNomeProduto.getText();
    double preco = Double.parseDouble(inputTextPrecoProduto.getText());
    int quantidade = Integer.parseInt(inputTextQuantProduto.getText());
    int qtdMin = Integer.parseInt(inputTextQtdMinProduto.getText());
    int qtdMax = Integer.parseInt(inputTextQtdMaxProduto.getText());
    String uniMedida = inputTextUniMedida.getText();

    int categoriaIndex = jComboBoxCategoria.getSelectedIndex();
    CategoriaDAO categoriaDAO = new CategoriaDAO();
    List<Categoria> categorias = categoriaDAO.listarCategorias();
    Categoria categoriaSelecionada = categorias.get(categoriaIndex);
    int categoriaId = categoriaSelecionada.getId();

    // Atualiza o objetoProduto (editando ou criando)
    objetoProduto.setNome(nome);
    objetoProduto.setPreco(preco);
    objetoProduto.setQuantidade(quantidade);
    objetoProduto.setQtd_minima(qtdMin);
    objetoProduto.setQtd_maxima(qtdMax);
    objetoProduto.setUni_medida(uniMedida);
    objetoProduto.setCategoria(categoriaSelecionada);
    objetoProduto.setCategoriaId(categoriaId);

    boolean sucesso;
    if (objetoProduto.getId() == 0) {
        sucesso = ProdutoDAO.insertProduto(objetoProduto);
    } else {
        sucesso = ProdutoDAO.atualizarProduto(objetoProduto);
    }

    if (sucesso) {
        JOptionPane.showMessageDialog(this, "Produto " + (objetoProduto.getId() == 0 ? "cadastrado" : "atualizado") + " com sucesso!");
        this.dispose();
    } else {
        JOptionPane.showMessageDialog(this, "Erro ao " + (objetoProduto.getId() == 0 ? "cadastrar" : "atualizar") + " produto.");
    }
    }//GEN-LAST:event_btnCadastroProdActionPerformed

