   public FormCadastrodeProdutos(Produto produto) {
    initComponents();
    this.objetoProduto = produto;
    setDefaultCloseOperation(FormCadastrodeProdutos.DISPOSE_ON_CLOSE);

    // Carregar categorias no combo box
    CategoriaDAO pesquisa = new CategoriaDAO();
    List<Categoria> categorias = pesquisa.listarCategorias();
    jComboBoxCategoria.removeAllItems();

    int categoriaSelecionadaIndex = -1;
    for (int i = 0; i < categorias.size(); i++) {
        Categoria c = categorias.get(i);
        jComboBoxCategoria.addItem(c.getNome());

        // Verifica se é a categoria do produto sendo editado
        if (c.getId() == produto.getCategoriaId()) {
            categoriaSelecionadaIndex = i;
        }
    }

    // Preencher os campos com os dados do produto recebido
    inputTextNomeProduto.setText(produto.getNome());
    inputTextPrecoProduto.setText(String.valueOf(produto.getPreco()));
    inputTextQuantProduto.setText(String.valueOf(produto.getQuantidade()));
    inputTextQtdMinProduto.setText(String.valueOf(produto.getQtd_minima()));
    inputTextQtdMaxProduto.setText(String.valueOf(produto.getQtd_maxima()));
    inputTextUniMedida.setText(produto.getUni_medida());

    // Selecionar categoria no combo box
    if (categoriaSelecionadaIndex >= 0) {
        jComboBoxCategoria.setSelectedIndex(categoriaSelecionadaIndex);
    }

    // Muda o texto do botão
    btnCadastroProd.setText("Atualizar");
}

    
